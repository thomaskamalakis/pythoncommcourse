import numpy as np

# list and arrays example

a = [1,2,3,4]                       # this is a list
print('Value of a = ',a)            

b = np.array(a)                     # this is a numpy array created from a list
print('Value of b = ',b)

c = np.array([4,5,6,7,8])           # this is another numpy array
print('Value of c = ', c)

d = list(c)                         # this is a list
print('Value of d = ',d) 

 